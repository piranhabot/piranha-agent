def create_builder_ui():
    """Create the No-Code Builder UI."""
    
    with gr.Blocks(title="Piranha No-Code Agent Builder", theme=gr.themes.Soft()) as ui:
        gr.Markdown("""
        # 🛠️ Piranha No-Code Agent Builder
        
        Build AI agents visually without writing code!
        
        **Features:**
        - 🎨 Drag-and-drop workflow builder
        - 📦 Pre-built templates
        - 🐍 Auto-generate Python code
        - 🚀 One-click deployment
        """)
        
        with gr.Row():
            with gr.Column(scale=1):
                gr.Markdown("### 📦 Templates")
                
                template_names = list(TEMPLATES.keys())
                template_dropdown = gr.Dropdown(
                    choices=template_names,
                    label="Select Template",
                    value=template_names[0] if template_names else None,
                )
                
                template_info = gr.Textbox(
                    label="Template Description",
                    lines=2,
                    interactive=False,
                )
                
                load_template_btn = gr.Button("Load Template", variant="primary")
                
                gr.Markdown("---")
                
                gr.Markdown("### ➕ Add Node")
                
                node_type = gr.Radio(
                    choices=["llm", "skill", "condition", "loop", "output"],
                    label="Node Type",
                    value="llm",
                )
                
                node_name = gr.Textbox(
                    label="Node Name",
                    placeholder="e.g., Analyze Input",
                )
                
                node_config = gr.Textbox(
                    label="Configuration (JSON)",
                    placeholder='{"prompt": "Your prompt here"}',
                    lines=3,
                )
                
                add_node_btn = gr.Button("Add Node", variant="secondary")
                
                gr.Markdown("---")
                
                gr.Markdown("### 🔗 Connect Nodes")
                
                with gr.Row():
                    source_node = gr.Dropdown(
                        choices=[],
                        label="From Node",
                    )
                    target_node = gr.Dropdown(
                        choices=[],
                        label="To Node",
                    )
                
                edge_label = gr.Textbox(
                    label="Connection Label (optional)",
                    placeholder="e.g., on_success",
                )
                
                connect_btn = gr.Button("Connect Nodes", variant="secondary")
            
            with gr.Column(scale=2):
                gr.Markdown("### 🎨 Workflow Canvas")
                
                workflow_canvas = gr.JSON(
                    label="Workflow",
                    value={"nodes": [], "edges": []},
                )
                
                with gr.Row():
                    export_json_btn = gr.Button("Export JSON", variant="secondary")
                    export_python_btn = gr.Button("Export Python", variant="secondary")
                    deploy_btn = gr.Button("🚀 Deploy Agent", variant="primary")
                
                gr.Markdown("---")
                
                gr.Markdown("### 📄 Generated Code")
                
                generated_code = gr.Code(
                    label="Python Code",
                    language="python",
                    lines=15,
                )
            
            with gr.Column(scale=1):
                gr.Markdown("### 📋 Node List")
                
                node_list = gr.JSON(
                    label="Current Nodes",
                    value=[],
                )
                
                gr.Markdown("---")
                
                gr.Markdown("### ⚙️ Agent Settings")
                
                agent_name = gr.Textbox(
                    label="Agent Name",
                    value="my-visual-agent",
                )
                
                agent_model = gr.Dropdown(
                    choices=[
                        "ollama/llama3:latest",
                        "gpt-4",
                        "claude-3-5-sonnet",
                        "gemini-pro",
                    ],
                    label="LLM Model",
                    value="ollama/llama3:latest",
                )
                
                agent_description = gr.Textbox(
                    label="Description",
                    lines=2,
                    placeholder="What does your agent do?",
                )
        
        # State
        workflow_state = gr.State(WorkflowBuilder())
        
        # Event handlers
        def update_template_info(selected):
            if selected and selected in TEMPLATES:
                return TEMPLATES[selected]["description"]
            return ""
        
        def load_template(selected, state: WorkflowBuilder):
            if not selected or selected not in TEMPLATES:
                return state, [], {}, "Select a template"
            
            template = TEMPLATES[selected]
            state = WorkflowBuilder()
            
            node_ids = []
            for i, node_data in enumerate(template.get("nodes", [])):
                node_id = state.add_node(
                    node_type=node_data["type"],
                    name=node_data["name"],
                    config=node_data.get("config", {}),
                )
                node_ids.append((node_id, node_data["name"]))
            
            for edge_data in template.get("edges", []):
                if edge_data["source"] < len(node_ids) and edge_data["target"] < len(node_ids):
                    state.connect(
                        node_ids[int(edge_data["source"])][0],
                        node_ids[int(edge_data["target"])][0],
                        edge_data.get("label", ""),
                    )
            
            node_choices = [f"{nid} - {name}" for nid, name in node_ids]
            
            return (
                state,
                node_choices,
                {"nodes": len(state.nodes), "edges": len(state.edges)},
                state.to_python_code(),
            )
        
        def add_node(node_type, name, config_json, state: WorkflowBuilder):
            try:
                config = json.loads(config_json) if config_json else {}
            except json.JSONDecodeError:
                return state, [], "Invalid JSON in config", []
            
            if not name:
                return state, [], "Node name is required", []
            
            node_id = state.add_node(node_type, name, config)
            
            node_choices = [f"{n.id} - {n.name}" for n in state.nodes]
            node_list = [{"id": n.id, "type": n.type, "name": n.name} for n in state.nodes]
            
            return (
                state,
                node_choices,
                f"✓ Added node: {name} ({node_id})",
                node_list,
            )
        
        def connect_nodes(source, target, label, state: WorkflowBuilder):
            if not source or not target:
                return state, "Select both source and target nodes"
            
            source_id = source.split(" - ")[0]
            target_id = target.split(" - ")[0]
            
            state.connect(source_id, target_id, label)
            
            return state, f"✓ Connected {source_id} → {target_id}"
        
        def export_workflow_json(state: WorkflowBuilder):
            return state.to_json()
        
        def export_workflow_python(state: WorkflowBuilder, name, model):
            code = state.to_python_code()
            
            # Update agent settings
            code = code.replace('name="visual-agent"', f'name="{name}"')
            code = code.replace('model="ollama/llama3:latest"', f'model="{model}"')
            
            return code
        
        def deploy_agent(state: WorkflowBuilder, name, model, description):
            """Generate deployment-ready agent code."""
            code = f'''#!/usr/bin/env python3
"""Auto-generated Agent: {name}

{description or "No description"}
"""

from piranha import Agent, Task

# Create agent
agent = Agent(
    name="{name}",
    model="{model}",
    description="{description or ""}",
)

# Workflow execution
'''
            code += state.to_python_code()
            code += '''

if __name__ == "__main__":
    print(f"✓ Agent '{agent.name}' deployed successfully!")
'''
            return code
        
        # Wire up events
        template_dropdown.change(
            fn=update_template_info,
            inputs=[template_dropdown],
            outputs=[template_info],
        )
        
        load_template_btn.click(
            fn=load_template,
            inputs=[template_dropdown, workflow_state],
            outputs=[workflow_state, source_node, workflow_canvas, generated_code],
        )
        
        add_node_btn.click(
            fn=add_node,
            inputs=[node_type, node_name, node_config, workflow_state],
            outputs=[workflow_state, source_node, gr.Textbox(label="Status"), node_list],
        )
        
        connect_btn.click(
            fn=connect_nodes,
            inputs=[source_node, target_node, edge_label, workflow_state],
            outputs=[workflow_state, gr.Textbox(label="Status")],
        )
        
        export_json_btn.click(
            fn=export_workflow_json,
            inputs=[workflow_state],
            outputs=[generated_code],
        )
        
        export_python_btn.click(
            fn=export_workflow_python,
            inputs=[workflow_state, agent_name, agent_model],
            outputs=[generated_code],
        )
        
        deploy_btn.click(
            fn=deploy_agent,
            inputs=[workflow_state, agent_name, agent_model, agent_description],
            outputs=[generated_code],
        )
    
    return ui


if __name__ == "__main__":
    ui = create_builder_ui()
    ui.launch(
        server_name="0.0.0.0",
        server_port=7861,
        inbrowser=True,
    )
