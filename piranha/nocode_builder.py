#!/usr/bin/env python3
"""No-Code Visual Agent Builder with Gradio.

Features:
- Drag-and-drop agent builder
- Visual workflow editor
- Pre-built templates
- One-click deployment
"""

import gradio as gr
import json
from typing import Any, Optional
from dataclasses import dataclass, asdict


@dataclass
class AgentNode:
    """Represents an agent node in the workflow."""
    id: str
    type: str  # "llm", "skill", "condition", "loop", "output"
    name: str
    config: dict
    position: tuple[int, int]


@dataclass
class WorkflowEdge:
    """Represents a connection between nodes."""
    source: str
    target: str
    label: str = ""


class WorkflowBuilder:
    """Builds agent workflows from visual configuration."""
    
    def __init__(self):
        self.nodes: list[AgentNode] = []
        self.edges: list[WorkflowEdge] = []
    
    def add_node(self, node_type: str, name: str, config: dict) -> str:
        """Add a node to the workflow."""
        import uuid
        node_id = str(uuid.uuid4())[:8]
        
        node = AgentNode(
            id=node_id,
            type=node_type,
            name=name,
            config=config,
            position=(0, 0),
        )
        
        self.nodes.append(node)
        return node_id
    
    def connect(self, source_id: str, target_id: str, label: str = "") -> None:
        """Connect two nodes."""
        self.edges.append(WorkflowEdge(
            source=source_id,
            target=target_id,
            label=label,
        ))
    
    def to_python_code(self) -> str:
        """Generate Python code from workflow."""
        lines = [
            "# Auto-generated by Piranha No-Code Builder",
            "from piranha import Agent, Task, Skill",
            "",
            "# Create agent",
            'agent = Agent(name="visual-agent", model="ollama/llama3:latest")',
            "",
            "# Workflow steps",
        ]
        
        # Sort nodes by position (top to bottom)
        sorted_nodes = sorted(self.nodes, key=lambda n: n.position[1])
        
        for i, node in enumerate(sorted_nodes):
            if node.type == "llm":
                lines.append(f"# Step {i+1}: {node.name}")
                prompt = node.config.get("prompt", "Process this")
                lines.append(f'result_{node.id} = agent.chat("{prompt}")')
            
            elif node.type == "skill":
                skill_name = node.config.get("skill", "unknown")
                lines.append(f"# Step {i+1}: Use {skill_name} skill")
                lines.append(f'skill_result_{node.id} = {skill_name}()')
            
            elif node.type == "condition":
                condition = node.config.get("condition", "True")
                lines.append(f"# Step {i+1}: Condition check")
                lines.append(f"if {condition}:")
                lines.append("    # Execute conditional branch")
            
            elif node.type == "output":
                lines.append(f"# Step {i+1}: Output")
                lines.append(f'print(f"Result: {{result_{node.id}}}")')
        
        return "\n".join(lines)
    
    def to_json(self) -> str:
        """Export workflow as JSON."""
        return json.dumps({
            "nodes": [asdict(n) for n in self.nodes],
            "edges": [asdict(e) for e in self.edges],
        }, indent=2)
    
    @classmethod
    def from_json(cls, json_str: str) -> "WorkflowBuilder":
        """Import workflow from JSON."""
        data = json.loads(json_str)
        
        builder = cls()
        
        for node_data in data["nodes"]:
            node = AgentNode(**node_data)
            builder.nodes.append(node)
        
        for edge_data in data["edges"]:
            edge = WorkflowEdge(**edge_data)
            builder.edges.append(edge)
        
        return builder


